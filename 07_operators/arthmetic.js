let a = 10;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

// Increment and Decrement
let c = 5;
console.log(c++);
console.log(++c);
console.log(c--);
console.log(--c);

// String concatenation
let str1 = "Hello";
let str2 = "World";
console.log(str1 + " " + str2);
